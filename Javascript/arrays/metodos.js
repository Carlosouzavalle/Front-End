//metodos de array 

//pop - ele remove o ultimo elemento do array
let nomes = ['carlos', 'ana', 'bia']
console.log(nomes)
nomes.pop()
console.log(nomes)

// push - add um elemento ao final do array
nomes.push('fernanda')
console.log(nomes)


// shift - ele remove um elemento do inicio do array
nomes.shift()
console.log(nomes)

// unshift - adiciona um elemento no inicio do array
nomes.unshift('patricia')
console.log(nomes)